    private void tampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tampilkanActionPerformed
String nama, nim, jurusan, angkatan, hasil;
    nim = txtnim.getText();
    nama = txtnama.getText();
    jurusan = cmbjurusan.getSelectedItem().toString();
    angkatan = cmbangkatan.getSelectedItem().toString();
    hasil = "Nim: " + nim + "\nNama: " + nama + "\nJurusan: " + jurusan + "\nAngkatan: " + angkatan;

    JOptionPane.showMessageDialog(null, hasil);

    try {
        simpanData(nim, nama, jurusan, angkatan); // Panggil metode untuk menyimpan data
        loadData(); // Muat ulang data setelah penyimpanan berhasil
    } catch (SQLException ex) {
        Logger.getLogger(biodata_mahasiswa.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memproses data. Silakan coba lagi.");
    }
}

// Metode untuk menyimpan data ke dalam database
private void simpanData(String nim, String nama, String jurusan, String angkatan) throws SQLException {
    Connection connection = koneksi_database.getConnection();
    String sql = "INSERT INTO tb_biodata (nim, nama, jurusan, angkatan) VALUES (?, ?, ?, ?)";
    PreparedStatement statement = connection.prepareStatement(sql);
    statement.setString(1, nim);
    statement.setString(2, nama);
    statement.setString(3, jurusan);
    statement.setString(4, angkatan);
    statement.executeUpdate();
    statement.close();
    connection.close();
    }//GEN-LAST:event_tampilkanActionPerformed

