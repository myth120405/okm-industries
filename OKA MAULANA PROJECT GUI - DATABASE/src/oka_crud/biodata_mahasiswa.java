    public void loadData(){
       model.getDataVector().removeAllElements();
       model.fireTableDataChanged();
        try{
            Connection c= koneksi_database.getConnection();
               Statement s = c.createStatement();
               String sql = "Select * from tb_biodata";
               ResultSet r =s.executeQuery(sql);
               while (r.next()){
                   Object[] o = new Object[4];//object ini berubah menjadi 4
                   o[0]=r.getString("nim");
                    o[1]=r.getString("nama");
                     o[2]=r.getString("jurusan");
                      o[3]=r.getString("angkatan");//object terbaru yaitu angkatan
                     model.addRow(o);
               }
               r.close();
               s.close();
        }catch (SQLException e){
            System.out.println("Terjadi error Database"+e);
        }
        
    }
  

